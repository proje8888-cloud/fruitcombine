function getSystemLanguage() {
    let browserLang = navigator.language || navigator.userLanguage || 'en';
    let langCode = browserLang.split('-')[0].toLowerCase();
    const supported = ['tr', 'en', 'az', 'ru', 'es', 'de', 'fr', 'pt', 'ar', 'it', 'zh', 'ja', 'ko', 'hi', 'id'];
    if (supported.includes(langCode)) return langCode;
    const langMap = { pt:'pt', br:'pt', es:'es', mx:'es', de:'de', at:'de', fr:'fr', ca:'fr', ar:'ar', sa:'ar', it:'it', zh:'zh', cn:'zh', ja:'ja', jp:'ja', ko:'ko', kr:'ko', hi:'hi', id:'id' };
    return langMap[langCode] || 'en';
}

